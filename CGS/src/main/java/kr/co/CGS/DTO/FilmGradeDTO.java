package kr.co.CGS.DTO;

public class FilmGradeDTO {

	private String member_ID;
	private String screening_ID;
	private int eval_grade;
	private String eval_content;
	private String eval_date;
	private String film_id;
	public String getMember_ID() {
		return member_ID;
	}
	public void setMember_ID(String member_ID) {
		this.member_ID = member_ID;
	}
	public String getScreening_ID() {
		return screening_ID;
	}
	public void setScreening_ID(String screening_ID) {
		this.screening_ID = screening_ID;
	}
	public int getEval_grade() {
		return eval_grade;
	}
	public void setEval_grade(int eval_grade) {
		this.eval_grade = eval_grade;
	}
	public String getEval_content() {
		return eval_content;
	}
	public void setEval_content(String eval_content) {
		this.eval_content = eval_content;
	}
	public String getEval_date() {
		return eval_date;
	}
	public void setEval_date(String eval_date) {
		this.eval_date = eval_date;
	}
	public String getFilm_id() {
		return film_id;
	}
	public void setFilm_id(String film_id) {
		this.film_id = film_id;
	}
	
	
	
	
}
